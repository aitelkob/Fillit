#include "libft.h"

char	*ft_strcpy(char *dst, const char *scr)
{
	int i;

	i = 0;
	while (scr[i] != '\0')
	{
		dst[i] = scr[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
