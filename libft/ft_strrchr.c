#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *t;

	t = (char *)s + ft_strlen(s);
	while (*t != c)
	{
		if (*t == *s)
		{
			return (NULL);
		}
		t--;
	}
	return (t);
}
